package com.example.espcontroller;

public class Stats {
    private String voltage;
    private String battery;
    private String rpm;
    private String rpm2;
    private String rpm3;
    private String rpm4;

    public Stats() {
    }

    public String getVoltage() {
        return voltage;
    }

    public void setVoltage(String voltage) {
        this.voltage = voltage;
    }

    public String getBattery() {
        return battery;
    }

    public void setBattery(String battery) {
        this.battery = battery;
    }

    public String getRpm() {
        return rpm;
    }

    public void setRpm(String rpm) {
        this.rpm = rpm;
    }
    public String getRpm2() {
        return rpm2;
    }

    public void setRpm2(String rpm2) {
        this.rpm2 = rpm2;
    }
    public String getRpm3() {
        return rpm3;
    }

    public void setRpm3(String rpm3) {
        this.rpm3= rpm3;
    }
    public String getRpm4() {
        return rpm4;
    }

    public void setRpm4(String rpm4) {
        this.rpm4 = rpm4;
    }
}
